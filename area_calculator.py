# Program: Rectangle Area Calculator
# Description: This program calculates the area of a rectangle 
#              based on user input.
# Author: Mohammed Hussin
# ========================================================

# Get dimensions from the user and convert inputs to float
length = float(input("Enter the length of the rectangle: "))
width = float(input("Enter the width of the rectangle: "))

# Inform the user that calculation is in progress
print("Please wait a second...")

# Calculate the area
area = length * width

# Display the final result
print(f"The area of the rectangle is: {area} cm² ")

